private void cbObjetoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cbObjetoMouseClicked
    
    Vector<Perfil> perfiles = null;
    Vector<Permiso> permisos = null;
    
    if (!(ultimoPerfil == null)) {
        
        perfiles = ControladorSeguridad.getInstancia().buscarPerfil(ultimoPerfil);
        permisos = perfiles.firstElement().getPermisos();
        
        if (!(permisos == null)) {
            
            for (int i = 0; i <= permisos.size() - 1; i++) {
            
                if ( permisos.elementAt(i).getObjeto().equals( cbObjeto.getSelectedItem().toString() ) ) {
                
                    jcbLeer.setSelected(permisos.elementAt(i).getLeer());
                    jcbInsertar.setSelected(permisos.elementAt(i).getInsertar());
                    jcbActualizar.setSelected(permisos.elementAt(i).getActualizar());
                    jcbBorrar.setSelected(permisos.elementAt(i).getBorrar());
    
                }
            }
        }
    }
}//GEN-LAST:event_cbObjetoMouseClicked

